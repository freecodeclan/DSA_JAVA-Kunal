package ObjectOrientedPrograming;

public class Human {
    public static class Human_new{
        int age;
        String name;
        int salary;
        boolean married;

        // Parameter Constructor
        public Human_new(int age, String name, int salary, boolean married){
            this.age = age;
            this.name = name;
            this.salary = salary;
            this.married = married;
        }
    }
}
